function getColor(d) {
    return d == 'Kuranji' ? '#F38484' :
           d == 'Lubuk Kilangan' ? '#D597F9' :
           d == 'Padang Timur' ? '#ACC715' :
           d == 'Padang Utara' ? '#EC9949' :
           d == 'Pauh' ? '#4C51EF' :
           d == 'Koto Tangah' ? '#EF4242' :
           d == 'Lubuk Begalung' ? '#EEF72E' :
           d == 'Padang Selatan' ? '#ACAF76' :
           d == 'Padang Barat' ? '#A53197' :
           d == 'Padang Barat' ? '#ff7800' :
           d == 'Nanggalo' ? '#ff7800' :
		'#59FD02';
}